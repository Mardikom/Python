погода = input("Какая сегодня погода? ")


if погода == "холодно":
    print("одень куртку")
elif погода == "тепло":
    print("одень футболку")
elif погода == "дождь":
    print("возьми зонт")
else:
    print("ну тогда я не знаю что одеть")






